     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $hp = $request->get("no_telp");
        $pass = md5($request->get("password"));
        $user = DB::select("select * from data_users where no_hp = '$hp' and password = '$pass'");
        if($user!=null) {
            return redirect('login')->with('success', 'Kamu berhasil Login :))');
        }
        else {
            return redirect('login')->with('failed', 'Kamu Gagal Login :((');
        }
        

        
    }

